let currentStep = 1;

function createServiceElements(step) {
    const servicesContainer = document.getElementById("services-container");
    servicesContainer.innerHTML = ""; // Clear previous data

    const data = dataByStep[step] || [];

    data.forEach(service => {
        const serviceDiv = document.createElement("div");
        serviceDiv.className = "servce-row";
        serviceDiv.innerHTML = `
                                                                                <div class="servce-inner">
                                                                                    <div class="inner-check">
                                                                                        <input type="checkbox" class="checkbox-servce" id="${service.name}" />
                                                                                        <label for="${service.name}" class="service-label">${service.name}</label>
                                                                                    </div>
                                                                                    <div class="service-price-duration">
                                                                                        <p class="service-price">${service.price}</p>
                                                                                    </div>
                                                                                </div>
                                                                                <div class="servce-duration-para">
                                                                                    <p>${service.duration}</p>
                                                                                </div>
                                                                            `;
        servicesContainer.appendChild(serviceDiv);
    });
}
createServiceElements(currentStep);
document.addEventListener('DOMContentLoaded', function () {
    createServiceElements(currentStep);

    const buttons = document.querySelectorAll('.button1');
    const checkboxes = document.querySelectorAll('.checkbox-servce');

    buttons.forEach(button => {
        button.addEventListener('click', function () {
            // Remove the "active-button" class from all buttons
            buttons.forEach(btn => btn.classList.remove('active-button'));

            // Add the "active-button" class to the clicked button
            this.classList.add('active-button');

            // Check if any checkbox is checked
            const isChecked = Array.from(checkboxes).some(checkbox => checkbox.checked);

            // Show or hide the popup based on checkbox status
            const popup = document.getElementById('popup');
            if (isChecked) {
                popup.style.display = 'block';
            } else {
                popup.style.display = 'none';
            }
        });
    });

    // Add event listeners for checkboxes
    checkboxes.forEach(checkbox => {
        checkbox.addEventListener('change', function () {
            // Check if any checkbox is checked
            const isChecked = Array.from(checkboxes).some(checkbox => checkbox.checked);

            // Show or hide the popup based on checkbox status
            const popup = document.getElementById('popup');
            if (isChecked) {
                popup.style.display = 'block';
            } else {
                popup.style.display = 'none';
            }
        });
    });
});
