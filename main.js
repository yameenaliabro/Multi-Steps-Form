const searchInput = document.getElementById('service-search');
const services = document.querySelectorAll('.servce-row');
// Add an input event listener to the search input
searchInput.addEventListener("input", function () {
    const searchValue = searchInput.value.toLowerCase();
    console.log("ðŸš€ ~ file: main.js:321 ~ searchValue:", searchValue)

    services.forEach(service => {
        const serviceName = service.querySelector('.service-label').textContent.toLowerCase();
        // Check if the service name includes the search value
        if (serviceName.includes(searchValue)) {
            service.style.display = 'block'; // Show the service
        } else {
            service.style.display = 'none'; // Hide the service
        }
    });
});


function nextStep(step) {
    if (step === currentStep) {
        if (step < 3) {
            document.querySelector(`.step:nth-child(${step})`).classList.remove("active");
            currentStep++;
            document.querySelector(`.step:nth-child(${currentStep})`).classList.add("active");
        } else {
            // Handle form submission here
            document.querySelector(`.step:nth-child(${currentStep})`).classList.remove("active");
            document.getElementById("result").classList.add("active");
        }
    }
}